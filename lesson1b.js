//  Arithmetic Operations
let a = 5;
let b = 4

// Addition;
console.log("the sum of the mumbers is: ", a + b);

// Subtraction
console.log("the difference of the mumbers is: ", a - b);

// Division
console.log("quotient of the mumbers is: ", a / b);

// Multiplication
